{
	static char szText[1024];
	
	va_list vaList;
	va_start(vaList, szMsg);
	vsprintf(szText, szMsg, vaList);
	va_end(vaList);

	return ((CWnd*)glb_GetMainFrame())->MessageBox(szText, "ב¾", iType);
}

